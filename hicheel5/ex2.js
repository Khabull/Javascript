// Жишээ нь: 5-ийн факториал: 5*4*3*2*1

let n = prompt("тоо оруулна уу");
let multiple = 1;
for (i = 1; i <= n; i++) {
  multiple *= i;
  //   console.log(multiple); //{Доторх программын бүх үр дүнг хэвлэнэ}
}
console.log(multiple); //{гаднах үр дүнг хэвлэх буюу зөвхөн multiple руу хэвлэж байгаа эцсийн үр дүнг хэвлэж байна.}
